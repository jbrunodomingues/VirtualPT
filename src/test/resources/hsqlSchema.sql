CREATE TABLE PERSON (
  ID_PERSON  INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 1
  INCREMENT BY 1 )       NOT NULL,
  FIRST_NAME VARCHAR(50) NOT NULL,
  LAST_NAME  VARCHAR(50) NOT NULL,
  PRIMARY KEY (ID_PERSON)
);

CREATE TABLE CLIENT (
  ID_PERSON INT NOT NULL,
  PRIMARY KEY (ID_PERSON),
  CONSTRAINT FK_CLIENT_1 FOREIGN KEY (ID_PERSON) REFERENCES PERSON (ID_PERSON)
);


CREATE TABLE PERSONAL_TRAINER (
  ID_PERSON INT NOT NULL,
  PRIMARY KEY (ID_PERSON),
  CONSTRAINT FK_PERSONAL_TRAINER_1 FOREIGN KEY (ID_PERSON) REFERENCES PERSON (ID_PERSON)
);

CREATE TABLE PERSONAL_TRAINER_CLIENT (
  REFID_PERSONAL_TRAINER INT NOT NULL,
  REFID_CLIENT           INT NOT NULL,
  PRIMARY KEY (REFID_PERSONAL_TRAINER, REFID_CLIENT),
  CONSTRAINT FK_PERSONAL_TRAINER_CLIENT_1 FOREIGN KEY (REFID_PERSONAL_TRAINER) REFERENCES PERSONAL_TRAINER (ID_PERSON),
  CONSTRAINT FK_PERSONAL_TRAINER_CLIENT_2 FOREIGN KEY (REFID_CLIENT) REFERENCES CLIENT (ID_PERSON)
);

